package com.mycompany.task_6;

public class Circle {
    private double r; 
         
        public void set_circle(double r) 
        { this.r=r; } 
         
        public double Area ( ) 
        { return (3.14*r*r ); } 
         
         public double Circumference () 
         { return (2*3.14*r ) ;  } 
}
